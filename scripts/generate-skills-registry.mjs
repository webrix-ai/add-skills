#!/usr/bin/env node

/**
 * Reads all skills/SKILL.md files at build time and generates
 * dist/sdk.mjs + dist/sdk.d.mts so consumers can:
 *
 *   import { skills, getSkillByName } from '@mcp-s/skills'
 */

import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { createHash } from 'crypto';
import { fileURLToPath } from 'url';
import matter from 'gray-matter';

const ROOT_DIRECTORY = join(dirname(fileURLToPath(import.meta.url)), '..');
const SKILLS_DIRECTORY = join(ROOT_DIRECTORY, 'skills');
const DIST_DIRECTORY = join(ROOT_DIRECTORY, 'dist');

function computeSignature(content) {
  return createHash('sha256').update(content).digest('hex');
}

function discoverAndParseSkills() {
  if (!existsSync(SKILLS_DIRECTORY)) {
    console.warn('Warning: skills/ directory not found');
    return [];
  }

  const entries = readdirSync(SKILLS_DIRECTORY, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => {
      const skillMarkdownPath = join(SKILLS_DIRECTORY, entry.name, 'SKILL.md');
      if (!existsSync(skillMarkdownPath)) return null;

      const content = readFileSync(skillMarkdownPath, 'utf-8');
      const { data: frontmatter } = matter(content);

      if (!frontmatter.name) {
        console.warn(`Skipping ${entry.name}: missing name in frontmatter`);
        return null;
      }

      const toStringArray = (value) => (Array.isArray(value) ? value : []);
      const toTrimmedString = (value) => {
        if (typeof value === 'string') return value.trim();
        if (value instanceof Date) return value.toISOString().slice(0, 10);
        return '';
      };

      return {
        slug: frontmatter.name,
        name: toTrimmedString(frontmatter.displayName) || frontmatter.name,
        tagline: toTrimmedString(frontmatter.tagline),
        description: toTrimmedString(frontmatter.description),
        department: toStringArray(frontmatter.department),
        useCases: toStringArray(frontmatter.use_cases),
        toolsRequired: toStringArray(frontmatter.tools_required),
        agentsCompatible: toStringArray(frontmatter.agents_compatible),
        author: toTrimmedString(frontmatter.author) || 'Community',
        verified: frontmatter.verified === true,
        updatedAt: toTrimmedString(frontmatter.updatedAt),
        exampleInput: toTrimmedString(frontmatter.exampleInput),
        exampleOutput: toTrimmedString(frontmatter.exampleOutput),
        content,
        version: toTrimmedString(frontmatter.version),
        signature: computeSignature(content),
      };
    })
    .filter((skill) => skill !== null)
    .sort((first, second) => first.slug.localeCompare(second.slug));
}

function generateEsmModule(skills) {
  return `// Auto-generated by scripts/generate-skills-registry.mjs — do not edit manually

const skills = ${JSON.stringify(skills, null, 2)};

export { skills };
export default skills;

export function getSkillBySlug(slug) {
  return skills.find((skill) => skill.slug === slug) ?? null;
}

export function getSkillByName(name) {
  return skills.find((skill) => skill.name === name || skill.slug === name) ?? null;
}

export function getSkillNames() {
  return skills.map((skill) => skill.name);
}

export function getSkillSlugs() {
  return skills.map((skill) => skill.slug);
}
`;
}

function generateTypeDefinitions() {
  return `// Auto-generated by scripts/generate-skills-registry.mjs — do not edit manually

export interface SkillEntry {
  readonly slug: string;
  readonly name: string;
  readonly tagline: string;
  readonly description: string;
  readonly department: readonly string[];
  readonly useCases: readonly string[];
  readonly toolsRequired: readonly string[];
  readonly agentsCompatible: readonly string[];
  readonly author: string;
  readonly verified: boolean;
  readonly updatedAt: string;
  readonly exampleInput: string;
  readonly exampleOutput: string;
  readonly content: string;
  readonly version: string;
  readonly signature: string;
}

export declare const skills: readonly SkillEntry[];
export default skills;

export declare function getSkillBySlug(slug: string): SkillEntry | null;
export declare function getSkillByName(name: string): SkillEntry | null;
export declare function getSkillNames(): string[];
export declare function getSkillSlugs(): string[];
`;
}

function main() {
  const skills = discoverAndParseSkills();

  if (!existsSync(DIST_DIRECTORY)) {
    mkdirSync(DIST_DIRECTORY, { recursive: true });
  }

  writeFileSync(join(DIST_DIRECTORY, 'sdk.mjs'), generateEsmModule(skills), 'utf-8');
  writeFileSync(join(DIST_DIRECTORY, 'sdk.d.mts'), generateTypeDefinitions(), 'utf-8');

  console.log(`Generated SDK with ${skills.length} skills`);
}

main();
